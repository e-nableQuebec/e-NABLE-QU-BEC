    const getRole = m => currentLang === 'fr' ? m.role_fr : m.role_en;
    const getBio = m => currentLang === 'fr' ? m.bio_fr : m.bio_en;

    let mOffset = 0;
    let filteredMembers = [...members];

    function getCardsPerView() {
      const w = window.innerWidth;
      if (w <= 580) return 1;
      if (w <= 900) return 2;
      return 3;
    }

    function getMemberStep() {
      const track = document.getElementById('memberTrack');
      const first = track.children[0];
      if (!first) return 0;
      return first.getBoundingClientRect().width + 16;
    }

    function renderDots() {
      const dots = document.getElementById('memberDots');
      dots.innerHTML = '';
      const cpv = getCardsPerView();
      const pages = Math.max(1, Math.ceil(filteredMembers.length / cpv));
      const curPage = Math.round(mOffset / Math.max(1, (getMemberStep() || 1) * cpv));

      for (let i = 0; i < pages; i++) {
        const d = document.createElement('div');
        d.className = 'member-dot' + (i === curPage ? ' active' : '');
        d.onclick = () => {
          mOffset = i * cpv * (getMemberStep() || 0);
          applyMemberOffset();
        };
        dots.appendChild(d);
      }
    }

    function applyMemberOffset() {
      const track = document.getElementById('memberTrack');
      const cpv = getCardsPerView();
      const step = getMemberStep();
      const maxOffset = Math.max(0, (filteredMembers.length - cpv) * step);
      mOffset = Math.max(0, Math.min(maxOffset, mOffset));
      track.style.transform = `translateX(-${mOffset}px)`;
      renderDots();
    }

    function renderMembers() {
      const query = (document.getElementById('search').value || '').toLowerCase();
      const allLabel = translations[currentLang].members_all;
      const activeChip = document.querySelector('.chip.active');
      const activeTeam = activeChip ? activeChip.textContent : allLabel;

      filteredMembers = members.filter(m => {
        const role = getRole(m);
        const teamOk = activeTeam === allLabel || role === activeTeam;
        const text = `${m.name} ${role} ${getBio(m)}`.toLowerCase();
        return teamOk && (!query || text.includes(query));
      });

      const track = document.getElementById('memberTrack');
      track.innerHTML = '';
      mOffset = 0;
      track.style.transform = 'translateX(0)';

      filteredMembers.forEach(m => {
        const card = document.createElement('article');
        card.className = 'member-card';
        card.innerHTML = `
          <div class="avatar"><img src="${m.photo}" alt="${m.name}" /></div>
          <b>${m.name}</b>
          <small>${getRole(m)}</small>
        `;
        card.onclick = () => openMember(m);
        track.appendChild(card);
      });

      renderDots();
    }

    function renderChips() {
      const chips = document.getElementById('chips');
      const allLabel = translations[currentLang].members_all;
      const roles = [
        allLabel,
        ...new Set(
          members
            .map(m => (getRole(m) || '').trim())
            .filter(role => role.length > 0)
        )
      ];
      chips.innerHTML = '';

      roles.forEach((r, i) => {
        const btn = document.createElement('button');
        btn.className = 'chip' + (i === 0 ? ' active' : '');
        btn.textContent = r;
        btn.type = 'button';
        btn.onclick = () => {
          document.querySelectorAll('.chip').forEach(c => c.classList.remove('active'));
          btn.classList.add('active');
          renderMembers();
        };
        chips.appendChild(btn);
      });
    }

    document.getElementById('memberPrev').onclick = () => {
      mOffset -= getMemberStep() * getCardsPerView();
      applyMemberOffset();
    };

    document.getElementById('memberNext').onclick = () => {
      mOffset += getMemberStep() * getCardsPerView();
      applyMemberOffset();
    };

    document.getElementById('search').oninput = renderMembers;
    window.addEventListener('resize', applyMemberOffset);

