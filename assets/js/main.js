    const io = new IntersectionObserver(entries => {
      entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('on'); });
    }, { threshold: 0.1 });
    document.querySelectorAll('.reveal').forEach(el => io.observe(el));

    const counterIO = new IntersectionObserver(entries => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          const el = e.target;
          const target = +el.dataset.count;
          const start = performance.now();
          const tick = now => {
            const p = Math.min(1, (now - start) / 900);
            el.textContent = Math.round(target * (1 - Math.pow(1 - p, 3)));
            if (p < 1) requestAnimationFrame(tick);
          };
          requestAnimationFrame(tick);
          counterIO.unobserve(el);
        }
      });
    }, { threshold: 0.7 });
    document.querySelectorAll('[data-count]').forEach(el => counterIO.observe(el));

    function scrollToSection(target) {
      if (!target) return;
      const nav = document.querySelector('.nav');
      const navHeight = nav ? nav.offsetHeight : 0;

      // Section blocks have large top padding; anchor to the visible section header instead.
      const anchor = target.matches('section')
        ? (target.querySelector('.section-header, .section-label, h1, h2') || target)
        : target;

      const rect = anchor.getBoundingClientRect();
      const absoluteTop = rect.top + window.pageYOffset;
      const top = Math.max(0, absoluteTop - navHeight - 6);
      window.scrollTo({ top, behavior: 'smooth' });
    }

    document.addEventListener('click', e => {
      const a = e.target.closest('a[href^="#"]');
      if (!a) return;
      const href = a.getAttribute('href');
      if (!href || href === '#') return;
      const target = document.querySelector(href);
      if (!target) return;
      e.preventDefault();
      scrollToSection(target);
      history.pushState(null, '', href);
    });

    window.addEventListener('load', () => {
      if (window.location.hash) {
        const target = document.querySelector(window.location.hash);
        if (target) {
          setTimeout(() => scrollToSection(target), 80);
        }
      }
    });

    const galleryTrack = document.getElementById('galleryTrack');
    const galleryPrev = document.getElementById('galleryPrev');
    const galleryNext = document.getElementById('galleryNext');

    let galleryAnimating = false;
    let galleryAuto = null;

    function updateGalleryClasses() {
      const items = Array.from(galleryTrack.children);
      items.forEach(item => item.classList.remove('is-left', 'is-center', 'is-right', 'is-hidden'));

      if (items[0]) items[0].classList.add('is-left');
      if (items[1]) items[1].classList.add('is-center');
      if (items[2]) items[2].classList.add('is-right');

      for (let i = 3; i < items.length; i++) {
        items[i].classList.add('is-hidden');
      }
    }

    function getGalleryStep() {
      const items = Array.from(galleryTrack.children);
      if (items.length < 2) return 0;
      const itemWidth = items[0].getBoundingClientRect().width;
      const gap = parseFloat(getComputedStyle(galleryTrack).gap) || 22;
      return itemWidth + gap;
    }

    function moveGalleryNext() {
      if (galleryAnimating) return;
      galleryAnimating = true;

      const step = getGalleryStep();
      galleryTrack.style.transition = 'transform .65s cubic-bezier(.22,.8,.22,1)';
      galleryTrack.style.transform = `translateX(-${step}px)`;

      galleryTrack.addEventListener('transitionend', function handler() {
        galleryTrack.removeEventListener('transitionend', handler);
        galleryTrack.style.transition = 'none';
        galleryTrack.appendChild(galleryTrack.firstElementChild);
        galleryTrack.style.transform = 'translateX(0)';
        updateGalleryClasses();

        requestAnimationFrame(() => {
          requestAnimationFrame(() => {
            galleryTrack.style.transition = '';
            galleryAnimating = false;
          });
        });
      }, { once: true });
    }

    function moveGalleryPrev() {
      if (galleryAnimating) return;
      galleryAnimating = true;

      const step = getGalleryStep();
      galleryTrack.style.transition = 'none';
      galleryTrack.prepend(galleryTrack.lastElementChild);
      galleryTrack.style.transform = `translateX(-${step}px)`;
      updateGalleryClasses();

      requestAnimationFrame(() => {
        requestAnimationFrame(() => {
          galleryTrack.style.transition = 'transform .65s cubic-bezier(.22,.8,.22,1)';
          galleryTrack.style.transform = 'translateX(0)';
        });
      });

      galleryTrack.addEventListener('transitionend', function handler() {
        galleryTrack.removeEventListener('transitionend', handler);
        galleryTrack.style.transition = '';
        galleryAnimating = false;
      }, { once: true });
    }

    function resetGalleryAuto() {
      clearInterval(galleryAuto);
      galleryAuto = setInterval(moveGalleryNext, 4500);
    }

    galleryNext.addEventListener('click', () => {
      moveGalleryNext();
      resetGalleryAuto();
    });

    galleryPrev.addEventListener('click', () => {
      moveGalleryPrev();
      resetGalleryAuto();
    });

    galleryTrack.addEventListener('click', e => {
      const slide = e.target.closest('.gallery-slide');
      if (!slide) return;

      const items = Array.from(galleryTrack.children);
      const index = items.indexOf(slide);

      if (index === 0) {
        moveGalleryPrev();
        resetGalleryAuto();
      } else if (index === 2) {
        moveGalleryNext();
        resetGalleryAuto();
      }
    });

    updateGalleryClasses();
    resetGalleryAuto();

